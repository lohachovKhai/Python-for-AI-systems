# Обчисліть факторіал для додатного числа n=(N+5)

# Рекурсивна функція для обчислення факторіалу числа
def factorial(n):
    if n == 0:
        return 1
    else:
        return n * factorial(n - 1)

N = int(input("Введіть значення N: "))
n = N + 5

print(f"Факторіал числа {n} дорівнює ", factorial(n))
